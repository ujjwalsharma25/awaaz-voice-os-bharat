/* ── Warm up the speech engine ─────────────────────────────────
   Chrome/Edge load their voice list asynchronously in the
   background. If speak() is called before that finishes, the
   browser can sit there for 1-3+ seconds before any sound comes
   out. Pinging getVoices() early (and listening for
   voiceschanged) means voices are almost always ready well
   before the user taps the mic, so real speak() calls can skip
   the safety delay entirely.                                    */
let _voicesReady = false;
if (typeof window !== "undefined" && window.speechSynthesis) {
  const _checkVoices = () => {
    if (window.speechSynthesis.getVoices().length > 0) _voicesReady = true;
  };
  _checkVoices();
  window.speechSynthesis.onvoiceschanged = _checkVoices;
}

/* Find the best installed voice for a given BCP47 code, e.g. "ta-IN".
   Browsers often DON'T auto-select a voice just from utt.lang — if no
   voice is explicitly assigned, many fall back to whatever voice is
   already active (commonly English or Hindi), no matter what lang
   tag is set. This is what caused every language to sound the same
   regardless of selection. Explicitly assigning utt.voice fixes it. */
const _findVoice = (bcp47) => {
  if (!window.speechSynthesis) return null;
  const voices = window.speechSynthesis.getVoices();
  if (!voices.length) return null;
  const base = bcp47.split("-")[0].toLowerCase();
  // 1) exact match (e.g. "ta-IN" === "ta-IN")
  let v = voices.find(x => x.lang?.toLowerCase() === bcp47.toLowerCase());
  // 2) same base language, any region (e.g. "ta-LK", "ta-SG")
  if (!v) v = voices.find(x => x.lang?.toLowerCase().split("-")[0] === base);
  return v || null;
};

/* ── Cloud voice fallback (Bhashini — Govt. of India NLP) ─────
   Used ONLY when no local voice exists for the requested language
   (see _findVoice above). Bhashini is the official, free Indian-
   government TTS/NLP service — see utils/bhashini.js for setup.
   If Bhashini isn't configured yet, this skips instantly with zero
   delay (no network attempt at all) and falls through to the
   browser's default voice, exactly like before. Once configured,
   it gives correct-language audio for languages with no local
   voice installed, at the cost of a short network round-trip.    */
const _playCloudAudio = (text, lang) => {
  return new Promise((resolve) => {
    if (!isBhashiniConfigured()) { resolve(false); return; }

    const timer = setTimeout(() => resolve(false), 6000);

    bhashiniTTS(text, lang)
      .then(base64Audio => {
        clearTimeout(timer);
        if (!base64Audio) { resolve(false); return; }
        try {
          const audio = new Audio(`data:audio/wav;base64,${base64Audio}`);
          let settled = false;
          const done = (ok) => { if (settled) return; settled = true; resolve(ok); };
          audio.onended = () => done(true);
          audio.onerror = () => done(false);
          audio.play().catch(() => done(false));
        } catch { resolve(false); }
      })
      .catch(() => { clearTimeout(timer); resolve(false); });
  });
};

/* ── Sequential audio queue ───────────────────────────────────
   Ensures only ONE utterance plays at a time across the whole
   app (homepage, confirmation step, form filling, complaint
   flow, chatbot, login, etc). Every speak() call is queued and
   the next item only starts after the previous one has fully
   finished (onend/onerror), preventing overlapping or
   out-of-order audio.                                          */
let _queue        = [];
let _queueRunning = false;

const _runQueue = () => {
  if (_queueRunning) return;
  const next = _queue.shift();
  if (!next) return;
  _queueRunning = true;

  const { text, lang, rate, pitch, resolve } = next;

  const finish = () => {
    _queueRunning = false;
    resolve();
    _runQueue();
  };

  if (!text || !window.speechSynthesis) { finish(); return; }

  const speakWithBrowser = (bcp47, voice) => {
    const utt   = new SpeechSynthesisUtterance(text);
    utt.lang    = bcp47;
    utt.rate    = rate;
    utt.pitch   = pitch;
    utt.volume  = 1;
    if (voice) utt.voice = voice;

    let settled = false;
    const done = () => { if (settled) return; settled = true; finish(); };
    utt.onend   = done;
    utt.onerror = done;

    window.speechSynthesis.speak(utt);
    setTimeout(done, 15000); // safety net if onend never fires
  };

  // Once voices have loaded (almost always true by the time a user
  // actually taps the mic), there's no need for any artificial delay.
  setTimeout(async () => {
    try {
      const bcp47 = LANG_BCP47[lang] || "hi-IN";

      // 1) Best case: a matching voice is already installed on this
      //    device/browser — instant, no network round-trip needed.
      const matchedVoice = _findVoice(bcp47);
      if (matchedVoice) { speakWithBrowser(bcp47, matchedVoice); return; }

      // 2) No local voice for this language — try cloud TTS so the
      //    correct language is still heard (accepting a short delay).
      const cloudOk = await _playCloudAudio(text, lang);
      if (cloudOk) { finish(); return; }

      // 3) Cloud unreachable too (offline / blocked / unsupported) —
      //    last resort: browser's own default voice, same as the
      //    original baseline behavior. Never worse than before.
      speakWithBrowser(bcp47, null);
    } catch { finish(); }
  }, _voicesReady ? 0 : 80);
};

/* Cancel any ongoing speech AND clear everything still queued.
   Used for deliberate interruptions (e.g. user starts the mic
   again) — not used between normal sequential messages.        */
export const stopSpeaking = () => {
  try {
    _queue.forEach(item => item.resolve());
    _queue = [];
    _queueRunning = false;
    window.speechSynthesis?.cancel();
  } catch {}
};

/* Core speak — queues the message and returns a Promise that
   resolves once THIS message has finished playing. Guaranteed
   to resolve even on error. */
export const speak = (text, lang = "hi", rate = 0.88, pitch = 1.0) => {
  return new Promise((resolve) => {
    _queue.push({ text, lang, rate, pitch, resolve });
    _runQueue();
  });
};

/* Speak OTP digit by digit slowly */
export const speakOTP = async (otp, lang = "hi") => {
  const digits  = otp.split("").join("... ");
  const prefix  = UI_TEXT[lang]?.otpPrefix || UI_TEXT.hi.otpPrefix;
  await speak(`${prefix} ${digits}`, lang, 0.65);
};

